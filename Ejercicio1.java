package com.mycompany.ejercicio1;
public class Ejercicio1 {

    public static void main(String[] args) {
        // Declaración e inicialización de variables
        int num1 = 10;
        int num2 = 5;

        // Suma
        int suma = num1 + num2;
        System.out.println("La suma de " + num1 + " y " + num2 + " es: "+ suma);

        // Resta
        int resta = num1 - num2;
        System.out.println("El resta de " + num1 + " y " + num2 + " es: "+ resta);

        // Multiplicación
        int multiplicacion = num1 * num2;
        System.out.println("La mult de " + num1 + " y " + num2 + " es:"+ multiplicacion);

        // División
        int division = num1 / num2;
        System.out.println("La division de " + num1 + " y " + num2 + " es:"+ division);

        // Módulo (Resto de la división)
        int modulo = num1 % num2;
        System.out.println("El resto de la división de " + num1 + " entre " + num2 + " es: " + modulo);
    }
}

